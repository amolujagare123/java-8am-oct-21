package ArraysDemos;

public class ArrayDemo3 {

    public static void main(String[] args) {

        char[] chAr = new char[5];

        chAr[0] = 'a';
        chAr[1] = 'b';
        chAr[2] = 'c';
        chAr[3] = 'd';
        chAr[4] = 'e';


        for(int i=0;i< chAr.length;i++)
        {
            System.out.println(chAr[i]);
        }


    }
}
